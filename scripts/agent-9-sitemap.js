"use strict";

const fs            = require("fs");
const path          = require("path");
const { SITES }     = require("./config");

const AGENT_NAME = "Agent 9: Sitemap Generator";

// Page priority mappings — higher = more important to Google
const PAGE_PRIORITIES = {
  "index.html":                      "1.0",
  "stamp-duty-calculator.html":      "0.9",
  "first-time-buyer-guide.html":     "0.8",
  "mortgage-deposit-guide.html":     "0.7",
  "conveyancing-guide.html":         "0.7",
  "shared-ownership-guide.html":     "0.7",
  "first-time-buyer-checklist.html": "0.7",
  "about.html":                      "0.4",
  "privacy.html":                    "0.3",
};

/**
 * Detects which site is running based on files present.
 * Returns the matching SITES entry or null.
 * @returns {object|null}
 */
function detectSite() {
  const files = fs.readdirSync(".");
  for (const [, site] of Object.entries(SITES)) {
    // Match by checking if any known page exists
    const knownPage = site.pages.find(p =>
      p.path !== "/" && p.path !== "/sitemap.xml" && p.path !== "/robots.txt" &&
      files.includes(p.path.replace("/", ""))
    );
    if (knownPage) return site;
    // Also match by index.html presence for simpler sites
    if (files.includes("index.html") && site.domain.includes(files.includes("stamp-duty-calculator.html") ? "ukfirst" : "smartsacrifice")) {
      return site;
    }
  }
  return null;
}

/**
 * Generates XML sitemap content for the given site and file list.
 * @param {object} site      - Site config from SITES
 * @param {string[]} htmlFiles - HTML filenames present in repo
 * @returns {string} XML content
 */
function generateSitemap(site, htmlFiles) {
  const today = new Date().toISOString().split("T")[0];

  const urls = htmlFiles
    .filter(f => !f.startsWith("404"))
    .sort((a, b) => {
      const pa = parseFloat(PAGE_PRIORITIES[a] || "0.5");
      const pb = parseFloat(PAGE_PRIORITIES[b] || "0.5");
      return pb - pa;
    })
    .map(filename => {
      const urlPath    = filename === "index.html" ? "/" : `/${filename}`;
      const priority   = PAGE_PRIORITIES[filename] || "0.5";
      const changefreq = parseFloat(priority) >= 0.8 ? "weekly"
                       : parseFloat(priority) >= 0.5 ? "monthly"
                       : "yearly";
      return [
        "  <url>",
        `    <loc>${site.baseUrl}${urlPath}</loc>`,
        `    <lastmod>${today}</lastmod>`,
        `    <changefreq>${changefreq}</changefreq>`,
        `    <priority>${priority}</priority>`,
        "  </url>",
      ].join("\n");
    })
    .join("\n");

  return [
    `<?xml version="1.0" encoding="UTF-8"?>`,
    `<!-- Generated by Beacon Agent 9 — do not edit manually -->`,
    `<!-- Last updated: ${new Date().toISOString()} -->`,
    `<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">`,
    urls,
    `</urlset>`,
  ].join("\n");
}

function run() {
  const line = "═".repeat(52);
  console.log(`\n${line}\nBEACON — ${AGENT_NAME}\nRun at: ${new Date().toISOString()}\n${line}\n`);

  const site = detectSite();
  if (!site) {
    console.log("⚠️  Cannot detect site — run from repo root directory");
    process.exit(0);
  }
  console.log(`✅ Site detected: ${site.name}\n`);

  const htmlFiles = fs.readdirSync(".").filter(f => f.endsWith(".html")).sort();
  console.log(`Found ${htmlFiles.length} HTML files`);

  const newContent = generateSitemap(site, htmlFiles);

  // Read existing to check if update needed
  let existing = "";
  try { existing = fs.readFileSync("sitemap.xml", "utf8"); } catch { /* new */ }

  // Compare without dynamic timestamps
  const strip = s => s.replace(/<!-- (Generated|Last) .*?-->/g, "").replace(/<lastmod>.*?<\/lastmod>/g, "");
  if (strip(newContent) === strip(existing)) {
    console.log("\n✅ Sitemap already current — no changes needed");
    process.exit(0);
  }

  fs.writeFileSync("sitemap.xml", newContent);
  console.log(`✅ Sitemap updated with ${htmlFiles.length} URLs`);

  // Update robots.txt sitemap reference if needed
  const sitemapRef = `Sitemap: ${site.baseUrl}/sitemap.xml`;
  try {
    let robots = fs.readFileSync("robots.txt", "utf8");
    if (!robots.includes(sitemapRef)) {
      fs.writeFileSync("robots.txt", robots.trim() + `\n${sitemapRef}\n`);
      console.log("✅ robots.txt updated");
    }
  } catch { /* robots.txt may not exist */ }

  process.exit(0);
}

run();
